interface TcpOp {
  op: "connect" | "send" | "recv" | "close";
  connId: string;
  host?: string;
  port?: number;
  dataBase64?: string;
  recvId?: number;
  bufsize?: number;
}

// TcpPoolDO: Holds persistent TCP connections across trampoline rounds.
// One instance per host:port pair. Connection persists in memory as long as the
// DO stays alive (70-140s idle timeout, extendable via alarm heartbeat).
// This eliminates the need to replay full TCP conversations each round.
export class TcpPoolDO extends DurableObject {
  private connections = new Map<string, {
    socket: any;
    reader: ReadableStreamDefaultReader<Uint8Array>;
    writer: WritableStreamDefaultWriter<Uint8Array>;
  }>();

  async tcpConnect(connId: string, host: string, port: number): Promise<void> {
    if (this.connections.has(connId)) return; // already connected
    if (isPrivateHost(host)) throw new Error(`tcp_connect blocked: private host ${host}`);
    const socket = connect({ hostname: host, port });
    const writer = socket.writable.getWriter();
    const reader = socket.readable.getReader();
    this.connections.set(connId, { socket, writer, reader });
  }

  async tcpSend(connId: string, dataBase64: string): Promise<void> {
    const conn = this.connections.get(connId);
    if (!conn) throw new Error(`send on unknown connection: ${connId}`);
    await conn.writer.write(base64Decode(dataBase64));
  }

  async tcpRecv(connId: string, bufsize: number): Promise<string> {
    const conn = this.connections.get(connId);
    if (!conn) throw new Error(`recv on unknown connection: ${connId}`);

    const timeoutPromise = new Promise<{ value: undefined; done: true }>((resolve) =>
      setTimeout(() => resolve({ value: undefined, done: true }), 10000)
    );

    const result = await Promise.race([conn.reader.read(), timeoutPromise]);
    if (result.done || !result.value) return "";

    const bytes = result.value.slice(0, bufsize);
    let binary = "";
    for (let i = 0; i < bytes.length; i++) binary += String.fromCharCode(bytes[i]);
    return btoa(binary);
  }

  async tcpClose(connId: string): Promise<void> {
    const conn = this.connections.get(connId);
    if (!conn) return;
    try { conn.writer.releaseLock(); } catch {}
    try { conn.reader.releaseLock(); } catch {}
    try { conn.socket.close(); } catch {}
    this.connections.delete(connId);
  }

  // Replay a full ops log in a single RPC session (1 billed request).
  // Returns a map of recvId → base64-encoded response data.
  async replayOps(ops: TcpOp[]): Promise<Record<number, string>> {
    const recvResults: Record<number, string> = {};

    for (const op of ops) {
      switch (op.op) {
        case "connect":
          await this.tcpConnect(op.connId, op.host!, op.port!);
          break;
        case "send":
          await this.tcpSend(op.connId, op.dataBase64!);
          break;
        case "recv": {
          const data = await this.tcpRecv(op.connId, op.bufsize || 65536);
          recvResults[op.recvId!] = data;
          break;
        }
        case "close":
          await this.tcpClose(op.connId);
          break;
      }
    }

    return recvResults;
  }
}

