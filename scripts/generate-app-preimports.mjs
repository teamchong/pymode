#!/usr/bin/env node
/**
 * Walk a Python project's source files, extract top-level package imports,
 * and emit a pymode_wizer_app_preimports.h header with `_preimport("X");`
 * lines for each.
 *
 * The wizer init function #includes this header at build time (gated by
 * PYMODE_APP_PREIMPORTS), baking the app's imports into the snapshot so
 * cold-start `import jinja2` etc. takes zero time at request time.
 *
 * Skip list comes from the user's pyproject.toml — nothing is hardcoded:
 *
 *   [tool.pymode]
 *   preimport_skip = ["pydantic", "numpy"]
 *
 * Modules listed there are NOT preimported into the wizer snapshot. They
 * load on first request from site-packages.zip. Use this for packages
 * heavy enough that baking them in pushes the worker past the 10MB limit.
 *
 * We shell out to python3 -c "import ast" because that's the only place
 * Python's import syntax is parseable accurately without re-implementing
 * the AST in JS.
 *
 * Usage: generate-app-preimports.mjs <project-dir> <output-header.h> [<entry.py>]
 */

import fs from "node:fs";
import path from "node:path";
import { execFileSync } from "node:child_process";

const [, , projectDir, outputHeader, entryPathArg] = process.argv;
if (!projectDir || !outputHeader) {
  console.error("usage: generate-app-preimports.mjs <project-dir> <output.h> [<entry.py>]");
  process.exit(2);
}

function readSkipFromPyproject(projectDir) {
  const pyproject = path.join(projectDir, "pyproject.toml");
  if (!fs.existsSync(pyproject)) return [];
  const content = fs.readFileSync(pyproject, "utf-8");
  const section = content.match(/\[tool\.pymode\]([\s\S]*?)(?=\n\[|\n*$)/);
  if (!section) return [];
  const skip = section[1].match(/preimport_skip\s*=\s*\[([\s\S]*?)\]/);
  if (!skip) return [];
  return skip[1]
    .split(",")
    .map(s => s.trim().replace(/^["']|["']$/g, ""))
    .filter(Boolean);
}

function collectImports(file) {
  const cmd = "import ast, sys, json; " +
    "tree = ast.parse(open(sys.argv[1]).read()); " +
    "names = set(); " +
    "for node in ast.walk(tree):\n" +
    "    if isinstance(node, ast.Import):\n" +
    "        for n in node.names: names.add(n.name.split('.')[0])\n" +
    "    elif isinstance(node, ast.ImportFrom):\n" +
    "        if node.module and node.level == 0: names.add(node.module.split('.')[0])\n" +
    "print(json.dumps(sorted(names)))";
  try {
    const stdout = execFileSync("python3", ["-c", cmd, file], { encoding: "utf-8" });
    return JSON.parse(stdout.trim());
  } catch (e) {
    console.error(`  WARN: failed to parse ${file}: ${e.message}`);
    return [];
  }
}

function walkPython(dir) {
  const out = [];
  if (!fs.existsSync(dir)) return out;
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      if (entry.name === "__pycache__" || entry.name === ".venv" || entry.name === "node_modules" || entry.name === ".pymode") {
        continue;
      }
      out.push(...walkPython(full));
    } else if (entry.name.endsWith(".py")) {
      out.push(full);
    }
  }
  return out;
}

const userSkip = new Set(readSkipFromPyproject(projectDir));

const pyFiles = walkPython(projectDir);
const imports = new Set();
const skipped = new Set();
for (const file of pyFiles) {
  for (const name of collectImports(file)) {
    if (!name) continue;
    if (userSkip.has(name)) {
      skipped.add(name);
      continue;
    }
    imports.add(name);
  }
}

const sorted = [...imports].sort();
const lines = [
  "/* Auto-generated by scripts/generate-app-preimports.mjs.",
  " * Lists the top-level modules this app's source code imports.",
  " * Included into pymode_wizer.c at build time (gated by",
  " * PYMODE_APP_PREIMPORTS) so the wizer snapshot pre-loads them.",
  " */",
  "",
];
for (const name of sorted) {
  const escaped = name.replace(/"/g, '\\"');
  lines.push(`_preimport("${escaped}");`);
}

// Preimport the user's entry module last, so its top-level statements
// execute at wizer time. The resulting Python objects live in the
// snapshot, so per-request handlers don't re-run module initialization.
if (entryPathArg) {
  const entryModule = entryPathArg
    .replace(/\.py$/, "")
    .replace(/\\/g, ".")
    .replace(/\//g, ".");
  lines.push("");
  lines.push("/* User's entry module — top-level execution baked into snapshot. */");
  lines.push(`_preimport("${entryModule}");`);
}

fs.mkdirSync(path.dirname(outputHeader), { recursive: true });
fs.writeFileSync(outputHeader, lines.join("\n") + "\n");
console.log(
  `Wrote ${outputHeader} — ${sorted.length} preimport(s)` +
  (entryPathArg ? ` + entry "${entryPathArg}"` : "") +
  `: ${sorted.join(", ") || "(none)"}`,
);
if (skipped.size > 0) {
  console.log(
    `  Skipped via pyproject.toml [tool.pymode] preimport_skip: ${[...skipped].sort().join(", ")}`,
  );
}
